"""Scaffold template registry and rendering (KF-8 first slice).

A scaffold template is a small, deterministic recipe: a tuple of files plus
the keywords that route a free-form prompt to it. Rendering is a plain
string-template ``$placeholder`` substitution — no Jinja, no LLM, no I/O
beyond writing the rendered files.

Three template families ship today, one per common stack shape:

* ``python-cli`` — pyproject.toml + entrypoint + README.
* ``node-cli``   — package.json + entrypoint + README.
* ``static-site`` — index.html + style.css + README.

The full KF-8 ticket calls for richer scaffolds (Next.js + SQLite, FastAPI
+ React, Streamlit) and a multi-stage plan; those land as follow-ups and
plug into this same registry.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path  # noqa: TC003 — used at runtime in materialize_template
from string import Template


@dataclass(frozen=True)
class ScaffoldFile:
    """One file produced by a scaffold template.

    Attributes:
        relative_path: POSIX-style path relative to the scaffold output
            directory (e.g. ``"src/main.py"``).
        content: File body. Substrings of the form ``$placeholder`` are
            substituted at render time using ``string.Template``.
    """

    relative_path: str
    content: str


@dataclass(frozen=True)
class ScaffoldTemplate:
    """Immutable description of one scaffold template.

    Attributes:
        name: Stable lookup key (e.g. ``"python-cli"``).
        description: Short human-readable summary for ``--help`` output.
        keywords: Lower-case tokens used by :func:`pick_template` to route
            free-form prompts. Order matters only for tie-breaking.
        files: Files materialised on disk, in deterministic order.
    """

    name: str
    description: str
    keywords: tuple[str, ...]
    files: tuple[ScaffoldFile, ...]


class ScaffoldError(RuntimeError):
    """Raised on user-facing scaffold errors (target dir not empty, etc.)."""


