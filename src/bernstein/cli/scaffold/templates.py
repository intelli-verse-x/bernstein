SCAFFOLD_TEMPLATES: dict[str, ScaffoldTemplate] = {
    _PYTHON_CLI.name: _PYTHON_CLI,
    _NODE_CLI.name: _NODE_CLI,
    _STATIC_SITE.name: _STATIC_SITE,
}

# Default fallback when no keyword matches the prompt.
_DEFAULT_TEMPLATE_NAME = "python-cli"


# ---------------------------------------------------------------------------
# Public helpers
# ---------------------------------------------------------------------------


def list_template_names() -> list[str]:
    """Return scaffold template keys in deterministic, sorted order."""
    return sorted(SCAFFOLD_TEMPLATES.keys())


def pick_template(prompt: str) -> ScaffoldTemplate:
    """Pick a scaffold template via a deterministic keyword heuristic.

    Each template lists keywords; we count how many distinct keywords appear
    as whole-word substrings of the lower-cased *prompt*. The template with
    the highest count wins; ties resolve by alphabetical name to keep the
    function fully deterministic. When no template scores a hit we fall back
    to ``python-cli`` as the most general default.

    Args:
        prompt: Free-form goal prompt from the user.

    Returns:
        The chosen :class:`ScaffoldTemplate`.
    """
    haystack = prompt.lower()
    scores: dict[str, int] = {}
    for name, template in SCAFFOLD_TEMPLATES.items():
        scores[name] = sum(1 for kw in template.keywords if kw in haystack)

    best_score = max(scores.values()) if scores else 0
    if best_score == 0:
        return SCAFFOLD_TEMPLATES[_DEFAULT_TEMPLATE_NAME]
    best_name = min(name for name, s in scores.items() if s == best_score)
    return SCAFFOLD_TEMPLATES[best_name]


def materialize_template(
    template: ScaffoldTemplate,
    dest: Path,
    *,
    prompt: str,
    force: bool = False,
) -> list[Path]:
    """Render *template* into *dest*, returning the absolute paths created.

    Substitutions: ``$prompt`` and ``$slug`` (derived from *dest*'s name).
    The slug is sanitised to alphanumerics and dashes so it is safe to use
    inside ``pyproject.toml`` / ``package.json`` ``name`` fields.

    Args:
        template: Template to render.
        dest: Output directory. Created if missing.
        prompt: Original user prompt (substituted into READMEs etc.).
        force: When False and *dest* already contains files, raise
            :class:`ScaffoldError` rather than silently overwriting.

    Returns:
        Absolute paths of files written, in template-declared order.

    Raises:
        ScaffoldError: When *dest* exists and is non-empty without *force*.
    """
    dest = dest.resolve()
    if dest.exists() and any(dest.iterdir()) and not force:
        raise ScaffoldError(
            f"Destination {dest} already exists and is not empty; pass --force to overwrite.",
        )
    dest.mkdir(parents=True, exist_ok=True)

    slug = _sanitize_slug(dest.name)
    mapping = {"prompt": prompt, "slug": slug}
    written: list[Path] = []
    for spec in template.files:
        rel = Template(spec.relative_path).safe_substitute(mapping)
        body = Template(spec.content).safe_substitute(mapping)
        target = dest / rel
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(body, encoding="utf-8")
        written.append(target)
    return written


# ---------------------------------------------------------------------------
# Internals
# ---------------------------------------------------------------------------


def _sanitize_slug(name: str) -> str:
    """Return *name* reduced to package-safe characters.

    Replaces non-alphanumeric runs with single dashes, lower-cases the
    result, and falls back to ``scaffold-app`` for empty inputs so we never
    emit an invalid ``pyproject.toml`` ``name`` field.
    """
    cleaned: list[str] = []
    prev_dash = False
    for ch in name.lower():
        if ch.isalnum():
            cleaned.append(ch)
            prev_dash = False
        elif not prev_dash:
            cleaned.append("-")
            prev_dash = True
    slug = "".join(cleaned).strip("-")
    return slug or "scaffold-app"
